//Variables-> let,const,var
//we cannot redeclare but can change the value
let full_name;//variable declaration 
full_name="Harika";//initialization (stored data)

//let full_name;//error if we do redeclaration
full_name="harika";//we can change the value
console.log(full_name);

//we can redeclare and change value
var last_name;
last_name="y"
last_name="Y";
last_name="yallanti"
console.log(last_name);

